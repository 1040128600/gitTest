package model;
//车辆购入主信息表
public class Bicycle_order {
	private Integer order_Id;
	private String oeder_Code;
	private Integer vender_Id;
	private String buy_Date;
	private Integer buy_Num;
	private Double buy_Price;
	private String person_In_Charge;
	private String invoice_No;
	private Integer user_Id;
	private String operator_Time;
	private String remark;
	private String status;
	@Override
	public String toString() {
		return "Bicycle_order [order_Id=" + order_Id + ", oeder_Code=" + oeder_Code + ", vender_Id=" + vender_Id
				+ ", buy_Date=" + buy_Date + ", buy_Num=" + buy_Num + ", buy_Price=" + buy_Price + ", person_In_Charge="
				+ person_In_Charge + ", invoice_No=" + invoice_No + ", user_Id=" + user_Id + ", operator_Time="
				+ operator_Time + ", remark=" + remark + ", status=" + status + "]";
	}
	public Integer getOrder_Id() {
		return order_Id;
	}
	public void setOrder_Id(Integer order_Id) {
		this.order_Id = order_Id;
	}
	public String getOeder_Code() {
		return oeder_Code;
	}
	public void setOeder_Code(String oeder_Code) {
		this.oeder_Code = oeder_Code;
	}
	public Integer getVender_Id() {
		return vender_Id;
	}
	public void setVender_Id(Integer vender_Id) {
		this.vender_Id = vender_Id;
	}
	public String getBuy_Date() {
		return buy_Date;
	}
	public void setBuy_Date(String buy_Date) {
		this.buy_Date = buy_Date;
	}
	public Integer getBuy_Num() {
		return buy_Num;
	}
	public void setBuy_Num(Integer buy_Num) {
		this.buy_Num = buy_Num;
	}
	public Double getBuy_Price() {
		return buy_Price;
	}
	public void setBuy_Price(Double buy_Price) {
		this.buy_Price = buy_Price;
	}
	public String getPerson_In_Charge() {
		return person_In_Charge;
	}
	public void setPerson_In_Charge(String person_In_Charge) {
		this.person_In_Charge = person_In_Charge;
	}
	public String getInvoice_No() {
		return invoice_No;
	}
	public void setInvoice_No(String invoice_No) {
		this.invoice_No = invoice_No;
	}
	public Integer getUser_Id() {
		return user_Id;
	}
	public void setUser_Id(Integer user_Id) {
		this.user_Id = user_Id;
	}
	public String getOperator_Time() {
		return operator_Time;
	}
	public void setOperator_Time(String operator_Time) {
		this.operator_Time = operator_Time;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public Bicycle_order(Integer order_Id, String oeder_Code, Integer vender_Id, String buy_Date, Integer buy_Num,
			Double buy_Price, String person_In_Charge, String invoice_No, Integer user_Id, String operator_Time,
			String remark, String status) {
		super();
		this.order_Id = order_Id;
		this.oeder_Code = oeder_Code;
		this.vender_Id = vender_Id;
		this.buy_Date = buy_Date;
		this.buy_Num = buy_Num;
		this.buy_Price = buy_Price;
		this.person_In_Charge = person_In_Charge;
		this.invoice_No = invoice_No;
		this.user_Id = user_Id;
		this.operator_Time = operator_Time;
		this.remark = remark;
		this.status = status;
	}
	public Bicycle_order() {
		super();
	}
	
	
}
