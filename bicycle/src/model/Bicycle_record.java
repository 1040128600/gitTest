package model;
//车辆——车辆租还记录
public class Bicycle_record {
	private Integer record_Id;
	private Integer bicycle_Id;
	private Integer card_Id;
	private String rent_Time;
	private Integer rent_Pile_Id;
	private String return_Time;
	private Integer return_Pile_Id;
	private Double money;
	private String remark;
	public Integer getRecord_Id() {
		return record_Id;
	}
	public void setRecord_Id(Integer record_Id) {
		this.record_Id = record_Id;
	}
	public Integer getBicycle_Id() {
		return bicycle_Id;
	}
	public void setBicycle_Id(Integer bicycle_Id) {
		this.bicycle_Id = bicycle_Id;
	}
	public Integer getCard_Id() {
		return card_Id;
	}
	public void setCard_Id(Integer card_Id) {
		this.card_Id = card_Id;
	}
	public String getRent_Time() {
		return rent_Time;
	}
	public void setRent_Time(String rent_Time) {
		this.rent_Time = rent_Time;
	}
	public Integer getRent_Pile_Id() {
		return rent_Pile_Id;
	}
	public void setRent_Pile_Id(Integer rent_Pile_Id) {
		this.rent_Pile_Id = rent_Pile_Id;
	}
	public String getReturn_Time() {
		return return_Time;
	}
	public void setReturn_Time(String return_Time) {
		this.return_Time = return_Time;
	}
	public Integer getReturn_Pile_Id() {
		return return_Pile_Id;
	}
	public void setReturn_Pile_Id(Integer return_Pile_Id) {
		this.return_Pile_Id = return_Pile_Id;
	}
	public Double getMoney() {
		return money;
	}
	public void setMoney(Double money) {
		this.money = money;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	@Override
	public String toString() {
		return "Bicycle_record [record_Id=" + record_Id + ", bicycle_Id=" + bicycle_Id + ", card_Id=" + card_Id
				+ ", rent_Time=" + rent_Time + ", rent_Pile_Id=" + rent_Pile_Id + ", return_Time=" + return_Time
				+ ", return_Pile_Id=" + return_Pile_Id + ", money=" + money + ", remark=" + remark + "]";
	}
	public Bicycle_record(Integer record_Id, Integer bicycle_Id, Integer card_Id, String rent_Time,
			Integer rent_Pile_Id, String return_Time, Integer return_Pile_Id, Double money, String remark) {
		super();
		this.record_Id = record_Id;
		this.bicycle_Id = bicycle_Id;
		this.card_Id = card_Id;
		this.rent_Time = rent_Time;
		this.rent_Pile_Id = rent_Pile_Id;
		this.return_Time = return_Time;
		this.return_Pile_Id = return_Pile_Id;
		this.money = money;
		this.remark = remark;
	}
	public Bicycle_record() {
		super();
	}
	
	
}
