		this.card_Code = card_Code;
		this.care_Type = care_Type;
		this.name = name;
		this.idcard = idcard;
		this.sex = sex;
		this.telphone = telphone;
		this.mobile = mobile;
		this.email = email;
		this.address = address;
		this.work = work;
		this.zxbj = zxbj;
		this.monthly_Money = monthly_Money;
		this.frozen_Money = frozen_Money;
		this.wallet_Money = wallet_Money;
		this.status = status;
