function setSelect(){
	var vender_Type= $("#vender_Type").val();
	if(vender_Type!=""){
		
		$("select[name='vender_Type'] option[value='"+vender_Type+"']").prop("selected",true);
	}
	
	var zxbj=$("#zxbj").val();
	if(zxbj!=""){
			$("select[name='zxbj'] option[value='"+zxbj+"']").prop("selected",true);
	}
}
$(function(){
	setSelect();
})